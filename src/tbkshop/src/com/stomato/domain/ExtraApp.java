package com.stomato.domain;

public class ExtraApp {
	private int id;
	
	private int order;
	
	private String name;
	
	private String size;
	
	private String version;
	
	private String downloadCounts;
	
	private String downloadUrl;
	
	private String description;
	
	private String previewsCounts;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getDownloadCounts() {
		return downloadCounts;
	}

	public void setDownloadCounts(String downloadCounts) {
		this.downloadCounts = downloadCounts;
	}

	public String getDownloadUrl() {
		return downloadUrl;
	}

	public void setDownloadUrl(String downloadUrl) {
		this.downloadUrl = downloadUrl;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getPreviewsCounts() {
		return previewsCounts;
	}

	public void setPreviewsCounts(String previewsCounts) {
		this.previewsCounts = previewsCounts;
	}
}
